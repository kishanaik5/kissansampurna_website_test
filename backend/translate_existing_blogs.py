def translate():
    db = SessionLocal()
    try:
        # Clear existing non-English blogs
        print("Cleaning old translations...")
        db.execute(text("DELETE FROM blogs_ks WHERE language != 'en'"))
        db.commit()

        for lang, blogs in TRANSLATIONS.items():
            print(f"Inserting {lang} blogs...")
            for i, blog_data in enumerate(blogs):
                new_blog = models.Blog(
                    id=uuid.uuid4(),
                    language=lang,
                    blog_title=blog_data["title"],
                    blog_body=blog_data["body"],
                    blog_img=IMAGES[i] if i < len(IMAGES) else None
                )
                db.add(new_blog)
        db.commit()
        print("Translations inserted successfully.")
    except Exception as e:
        print(f"Error during translation: {e}")
        traceback.print_exc()
        db.rollback()
    finally:
        db.close()

if __name__ == "__main__":
    translate()
